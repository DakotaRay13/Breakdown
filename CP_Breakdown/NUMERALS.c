//Digit Functions

//Reset Score Digits
void ResetScoreDigits()
{
    set_win_data(DGT_SCORE_1, 1, DIGITS[0]);
    set_win_data(DGT_SCORE_2, 1, DIGITS[0]);
    set_win_data(DGT_SCORE_3, 1, DIGITS[0]);
    set_win_data(DGT_SCORE_4, 1, DIGITS[0]);
    set_win_data(DGT_SCORE_5, 1, DIGITS[0]);
    set_win_data(DGT_SCORE_6, 1, DIGITS[0]);
}

void UpdateScoreDigits(UINT8 score[6])
{
    //Current Scoreboard
    set_win_data(DGT_SCORE_1, 1, DIGITS[score[0]]);
    set_win_data(DGT_SCORE_2, 1, DIGITS[score[1]]);
    set_win_data(DGT_SCORE_3, 1, DIGITS[score[2]]);
    set_win_data(DGT_SCORE_4, 1, DIGITS[score[3]]);
    set_win_data(DGT_SCORE_5, 1, DIGITS[score[4]]);
    set_win_data(DGT_SCORE_6, 1, DIGITS[score[5]]);
}

void UpdateHighScoreDigits(UINT8 highScore[6])
{
    set_win_data(DGT_HIGH_1, 1, DIGITS[highScore[0]]);
    set_win_data(DGT_HIGH_2, 1, DIGITS[highScore[1]]);
    set_win_data(DGT_HIGH_3, 1, DIGITS[highScore[2]]);
    set_win_data(DGT_HIGH_4, 1, DIGITS[highScore[3]]);
    set_win_data(DGT_HIGH_5, 1, DIGITS[highScore[4]]);
    set_win_data(DGT_HIGH_6, 1, DIGITS[highScore[5]]);
}